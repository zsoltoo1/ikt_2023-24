szam = input("Adjon meg egy számot 1-12 között: ")
szam = int(szam)
if szam == 1:
    print(f"Január")
elif szam == 2:
    print(f"Február")
elif szam == 3:
    print(f"Március")
elif szam == 4:
    print(f"Április")
elif szam == 5:
    print(f"Május")
elif szam == 6:
    print(f"Június")
elif szam == 7:
    print(f"Július")
elif szam == 8:
    print(f"Augusztus")
elif szam == 9:
    print(f"Szeptember")
elif szam == 10:
    print(f"Október")
elif szam == 11:
    print(f"November")
else:
    print(f"1-12 közötti számot kér!")